class Calculator:
    def __init__(self):
        pass

    def addition(self, a, b):
        return float(a) + float(b)

    def subtract(self, a, b):
        return float(a) - float(b)

    def multiplication(self, a, b):
        return float(a) * float(b)

    def division(self, a, b):
        return float(b) / float(a)

    def squareroot(self, a):
        return float(a ** 0.5)

    def square(self, a):
        return float(a) * float(a)
